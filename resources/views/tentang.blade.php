                <div class="col-md-12 col-sm-12">
                    <p><b>Desa Badak Mekar</b>, yang terletak di Kecamatan Muara Badak, Kabupaten Kutai Kartanegara,
                        Provinsi Kalimantan Timur, resmi menjadi desa pecahan dari Desa Muara Badak Ilir pada tahun
                        2003. Pemekaran ini dimulai dengan Pak Ardianus sebagai Penjabat Sementara (PJ) atau
                        Sekretaris Desa, dan Pak Herul sebagai Kepala Desa dalam masa persiapan. Nama Desa Badak
                        Mekar dipilih melalui musyawarah dengan beberapa tokoh masyarakat setempat, termasuk Pak Sadiq,
                        Pak Markus Kolondam, Almarhum Pak Mahmud, Pak Syarifudin, dan Almarhum Pak Ardianus.</p>
                    <p>Pada Tahun 2004, Desa Badak Mekar resmi menjadi desa definitif dengan Ibu Sofia
                        sebagai Kepala Desa, menjabat selama dua periode atau 13 tahun. Struktur pemerintahan desa
                        juga berubah dengan Bu Narti sebagai Sekdes, Musriadi sebagai Kaur Pembangunan, Pak
                        Syarifudin sebagai Kaur Pemerintahan, dan Asnia sebagai Kaur Umum. Pada tahun 2006, kantor
                        desa pindah ke lokasi yang sekarang, dan jabatan Bendahara dipegang oleh Ibu Munah hingga
                        tahun 2008. Pembangunan infrastruktur desa seperti kantor desa, gedung PKK, dan fasilitas
                        lainnya gencar dilakukan antara tahun 2007 dan 2008.
                    </p>
                    <p>
                        Pada tahun 2009, terjadi perekrutan staf baru dan Ibu Sofia kembali terpilih sebagai Kepala
                        Desa. Tahun 2010 menandai pergantian jabatan Sekretaris Desa yang berlangsung hingga tahun
                        2016. Setelah pengunduran diri Ibu Sofia pada tahun 2014, posisi tersebut diisi oleh
                        Penjabat (PJ) hingga tahun 2016, ketika Pak Herul mulai menjabat sebagai Kepala Desa. Pada
                        tahun yang sama, struktur jabatan desa diperbarui dengan pembentukan posisi Kepala Urusan dan
                        Kepala Seksi,
                        termasuk Kepala Seksi Pemerintahan, Kepala Seksi Kesra, Kepala Urusan Umum, Kepala Urusan
                        Perencanaan, serta Kepala Urusan Keuangan.
                    </p>
                    <p>
                        Pada tahun 2022 hingga sekarang, kepemimpinan desa berpindah ke Pak Budi Ilhamdi. Desa Badak
                        Mekar terus berkembang dengan
                        komitmen memberikan pelayanan terbaik
                        bagi masyarakatnya.
                    </p>
